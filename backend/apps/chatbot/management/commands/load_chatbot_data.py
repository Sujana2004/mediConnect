"""
Load Chatbot Sample Data
========================
Loads FAQs, Health Tips, and Quick Replies into the database.
Supports English, Telugu, and Hindi.
"""

from django.core.management.base import BaseCommand
from django.db import transaction
from apps.chatbot.models import FAQ, HealthTip, QuickReply


class Command(BaseCommand):
    help = 'Load sample chatbot data (FAQs, Health Tips, Quick Replies)'

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear',
            action='store_true',
            help='Clear existing data before loading',
        )

    def handle(self, *args, **options):
        if options['clear']:
            self.stdout.write('Clearing existing chatbot data...')
            FAQ.objects.all().delete()
            HealthTip.objects.all().delete()
            QuickReply.objects.all().delete()
            self.stdout.write(self.style.WARNING('Existing data cleared.'))

        with transaction.atomic():
            self.load_faqs()
            self.load_health_tips()
            self.load_quick_replies()

        self.stdout.write(self.style.SUCCESS('\n‚úÖ All chatbot data loaded successfully!'))
        self.print_summary()

